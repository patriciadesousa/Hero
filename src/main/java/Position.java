public class Position {
    private int x;
    private int y;

    public Position(int m, int n){
        x = m;
        this.y = n;
    }
    public int getX(){
        return x;
    }
    public void setX(int x){
        this.x = x;
    }
    public void setY (int y){
        this.y = y;
    }

    public int getY() {
        return getY();
    }
}
